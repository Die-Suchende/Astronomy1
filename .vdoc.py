from numpy import pi, sqrt
from astropy.constants import G, M_sun

R = 10e3 #m
Gval = G.value #m^3/(kg s^2)
M = 3*M_sun.value #kg
tau_min = 2*pi*sqrt(R**3/(Gval*M)) #s
print(f"The minimum rotation period of the neutron star is {tau_min:.3e} s")

